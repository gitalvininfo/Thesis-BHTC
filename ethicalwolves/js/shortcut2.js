document.body.addEventListener("keydown", function (event) {
	if (event.keyCode === 112) {
		window.location.href="dashboard_medtech.php";
	}
	if (event.keyCode === 113) {
		window.location.href="medtech_patient_master_file.php";
	}
	if (event.keyCode === 114) {
		window.location.href="medtech_laboratory_request.php";
	}
	if (event.keyCode === 115) {
		window.location.href="medtech_examination_reports.php";
	}	
	if (event.keyCode === 117) {
		window.location.href="examination_reports.php";
	}		});